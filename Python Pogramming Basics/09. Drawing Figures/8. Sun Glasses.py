n = int(input())

print("*" * (n * 2) + " " * n + "*" * (n * 2))

for i in range(0, n-2):
    if i == (n - 3) // 2:
        print("*" + "/" * (n * 2 - 2) + "*" + "|" * n + "*" + "/" * (n * 2 - 2) + "*")
    else:
        print("*" + "/" * (n * 2 - 2) + "*" + " " * n + "*" + "/" * (n * 2 - 2) + "*")

print("*" * (n * 2) + " " * n + "*" * (n * 2))
